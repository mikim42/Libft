#include "libft.h"

int		ft_atoi(const char *s)
{
	int	res;
	int	np;

	np = 1;
	res = 0;
	while (*s == '\t' || *s == ' ')
		s++;
	*s == '-' ? np = -1 : 0;
	*s == '-' || *s == '+' ? s++ : 0;
	while (*s >= '0' && *s <= '9')
	{
		res *= 10;
		res += *s - '0';
		++s;
	}
	return (res *= np);
}
