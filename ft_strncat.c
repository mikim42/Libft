#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	j;

	i = -1;
	j = -1;
	while (s1[++i])
		;
	while (++j < n && s2[j] != '\0')
	{
		s1[i] = s2[j];
		++i;
	}
	s1[i] = '\0';
	return (s1);
}
